#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int j;
	int k;

	i = 0;
	j = 0;
	k = 1;
	while (str[i] != '\0' && (ft_isspace(str[i]) == 1))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			k = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		j = j * 10 + (str[i] - 48);
		i++;
	}
	return (j * k);
}
