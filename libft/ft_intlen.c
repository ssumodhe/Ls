#include "libft.h"

int		ft_intlen(int n)
{
	int n_int;

	n_int = 0;
	if (n == 0)
		return (n_int = 1);
	if (n < 0)
		n_int++;
	while (n != 0)
	{
		n = n / 10;
		n_int++;
	}
	return (n_int);
}
