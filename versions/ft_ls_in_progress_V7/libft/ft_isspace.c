#include "libft.h"

int		ft_isspace(char str)
{
	if (str == ' ' || str == '\t' || str == '\n' || str == '\a' \
			|| str == '\b' || str == '\f' || str == '\r' || str == '\v')
		return (1);
	return (0);
}
